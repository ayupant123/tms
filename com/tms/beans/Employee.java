package com.tms.beans;

public class Employee {
    
	private String empId;
	private String empFirstname;
	private String empLastName;
	private String empEmail;
	private String empPassword;
	private int empMobileNo;
	private String empDob;
    private String empMailingAddress;
    private String empPermanentAddress;
    private double empSalary;
	public String getEmpId() {
		return empId;
	}
	public void setEmpId(String empId) {
		this.empId = empId;
	}
	public String getEmpFirstname() {
		return empFirstname;
	}
	public void setEmpFirstname(String empFirstname) {
		this.empFirstname = empFirstname;
	}
	public String getEmpLastName() {
		return empLastName;
	}
	public void setEmpLastName(String empLastName) {
		this.empLastName = empLastName;
	}
	public String getEmpEmail() {
		return empEmail;
	}
	public void setEmpEmail(String empEmail) {
		this.empEmail = empEmail;
	}
	public String getEmpPassword() {
		return empPassword;
	}
	public void setEmpPassword(String empPassword) {
		this.empPassword = empPassword;
	}
	public int getEmpMobileNo() {
		return empMobileNo;
	}
	public void setEmpMobileNo(int empMobileNo) {
		this.empMobileNo = empMobileNo;
	}
	public String getEmpDob() {
		return empDob;
	}
	public void setEmpDob(String empDob) {
		this.empDob = empDob;
	}
	public String getEmpMailingAddress() {
		return empMailingAddress;
	}
	public void setEmpMailingAddress(String empMailingAddress) {
		this.empMailingAddress = empMailingAddress;
	}
	public String getEmpPermanentAddress() {
		return empPermanentAddress;
	}
	public void setEmpPermanentAddress(String empPermanentAddress) {
		this.empPermanentAddress = empPermanentAddress;
	}
	public double getEmpSalary() {
		return empSalary;
	}
	public void setEmpSalary(double empSalary) {
		this.empSalary = empSalary;
	}
    
    
	
	
}
